package cmd

import (
	"os"

	"github.com/spf13/cobra"
)

func init() {
	rootCmd.AddCommand(completionCmd)
}

var completionCmd = &cobra.Command{
	Use:   "completion [bash|zsh|fish|powershell]",
	Short: "Generate shell completion scripts",
	Long: `Generate shell completion scripts for bcp.

To load completions:

Bash:
  $ source <(bcp completion bash)

  # To load completions for each session, execute once:
  # Linux:
  $ bcp completion bash > /etc/bash_completion.d/bcp
  # macOS:
  $ bcp completion bash > $(brew --prefix)/etc/bash_completion.d/bcp

Zsh:
  # If shell completion is not already enabled in your environment,
  # you will need to enable it. You can execute the following once:
  $ echo "autoload -U compinit; compinit" >> ~/.zshrc

  # To load completions for each session, execute once:
  $ bcp completion zsh > "${fpath[1]}/_bcp"

  # You will need to start a new shell for this setup to take effect.

Fish:
  $ bcp completion fish | source

  # To load completions for each session, execute once:
  $ bcp completion fish > ~/.config/fish/completions/bcp.fish

PowerShell:
  PS> bcp completion powershell | Out-String | Invoke-Expression

  # To load completions for every new session, run:
  PS> bcp completion powershell > bcp.ps1
  # and source this file from your PowerShell profile.
`,
	DisableFlagsInUseLine: true,
	ValidArgs:             []string{"bash", "zsh", "fish", "powershell"},
	Args:                  cobra.ExactValidArgs(1),
	Run: func(cmd *cobra.Command, args []string) {
		switch args[0] {
		case "bash":
			cmd.Root().GenBashCompletion(os.Stdout)
		case "zsh":
			cmd.Root().GenZshCompletion(os.Stdout)
		case "fish":
			cmd.Root().GenFishCompletion(os.Stdout, true)
		case "powershell":
			cmd.Root().GenPowerShellCompletionWithDesc(os.Stdout)
		}
	},
}
